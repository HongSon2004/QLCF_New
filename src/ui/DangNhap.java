    private boolean kiemtrarong(){
        if(txtUser.getText().equals("")){
           MsgBox.alert(this, "Vui Long tên đăng nhập!");
            return false;
        }
        else
        if(txtSDT.getText().equals("")){
            MsgBox.alert(this, "Vui long nhap so dien thoai!");
            return false;
        }
        return true;
    }
